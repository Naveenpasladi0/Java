package operators;

public class arithmeticoperator {
    public static void main(String[] args) {
        int a = 20;
        int b = 30;
        int c = 40;
        System.out.println(a+b+c); // Addition
        System.out.println(a-b-c); // Subtraction
        System.out.println(a*b*c); // Multiplication
        System.out.println(0); // Divided by with 0
        System.out.println(a%b%c); // Percentage or Average
    }
}
